package com.yedam.java.ch0605;

public class Student {
	// 필드
	String sName; // 학생의 이름
	String school;// 학교 이름
	int stNo; // 학번
	int grade; // 학년
	int kor; // 국어, 영어, 수학 성적
	int eng;
	int mat;
	
	
	// 생성자
	public String getsName() {
		return sName;
	}

	public String getSchool() {
		return school;
	}

	public int getStNo() {
		return stNo;
	}

	public int getGrade() {
		return grade;
	}

	public int getKor() {
		return kor;
	}

	public int getEng() {
		return eng;
	}

	public int getMat() {
		return mat;
	}

	
	
	
	// 메소드
	
	
}
